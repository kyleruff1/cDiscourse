/**
 * MCP-014 — Referee feedback banner library: the hand-authored string library.
 *
 * `REFEREE_BANNER_LIBRARY` is the >=103-entry banner library (MCP-008 §5.1
 * category floors). Every entry describes a STRUCTURAL play property of the
 * MOVE — never the person, never a verdict. Each entry's `accessibilityLabel`
 * is produced by `buildBannerAccessibilityLabel` at module load so the label
 * can never drift from the headline.
 *
 * Pure TypeScript. No network. No Supabase. No React. No runtime cross-feature
 * import except the local `accessibilityLabel` helper.
 *
 * Doctrine (MCP-014 design § "Doctrine self-check"):
 *   - No banner string carries a verdict / winner / loser / truth / person
 *     label / popularity-praise token (enforced by `refereeBannerBanList`).
 *   - Every celebratory / confident banner that could over-claim on a
 *     low-confidence signal has a softened `*_soft` sibling whose headline
 *     hedges (`looks like` / `might` / `reads as` / `may`).
 *   - Softened siblings have `minConfidence: 'low'` and
 *     `softenedSiblingCode: null` — no infinite chain.
 *   - `contains_unplayable_insult_only` is intentionally silent — the move is
 *     never labelled "an insult" (`INTENTIONALLY_SILENT_CLASSIFIERS`).
 */

