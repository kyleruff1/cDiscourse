/**
 * The frozen banner library. Each entry's `accessibilityLabel` is generated
 * from `headline` + `helperLine` + `tone` at module load by
 * `buildBannerAccessibilityLabel` — so the label can never drift from the
 * visible copy. Every entry is `Object.freeze`d.
 */
export const REFEREE_BANNER_LIBRARY: ReadonlyArray<RefereeBanner> = Object.freeze(
  BANNER_SEEDS.map((seed) =>
    Object.freeze({
      ...seed,
      accessibilityLabel: buildBannerAccessibilityLabel(seed),
    }),
  ),
);

/** O(1) lookup index — `bannerCode` → `RefereeBanner`. */
export const BANNER_BY_CODE: ReadonlyMap<string, RefereeBanner> = (() => {
  const map = new Map<string, RefereeBanner>();
  for (const banner of REFEREE_BANNER_LIBRARY) {
    map.set(banner.bannerCode, banner);
  }
  return map;
})();

